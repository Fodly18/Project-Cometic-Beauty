     public void totalBiaya() {
    int jumlahBaris = tabel.getRowCount();
        int totalBiaya = 0;
        int jumlahBarang;
        for (int i = 0; i < jumlahBaris; i++) {
            jumlahBarang = Integer.parseInt(tabel.getValueAt(i, 4).toString());
            totalBiaya = totalBiaya + jumlahBarang ;
        }
        txtotal.setText(String.valueOf(totalBiaya));
        txtotal.setText("Rp "+ totalBiaya +",00");
    }

public void datatable() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode");
        tbl.addColumn("Jumlah");
        tbl.addColumn("Tanggal");
        tbl.addColumn("Keterangan");
        tbl.addColumn("Harga");
        tabel.setModel(tbl);
        try {
            Statement statement = (Statement) Conek.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("SELECT * FROM pengeluaran");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("kode"),
                    res.getString("jumlah"),
                    res.getString("tanggal"),
                    res.getString("keterangan"),
                    res.getString("harga")
                });
            }
            res.close();
            statement.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error memuat data: " + e.getMessage());
        }
    }

public void tanggal(){
        Date now = new Date();
        txttanggal.setDate(now);
        txttanggal.setEnabled(false);
}
