    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here: 
        String kode = txtkode.getText();
        if (kode.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Statement statement = (Statement) Conek.GetConnection().createStatement();
                int rowsDeleted = statement.executeUpdate("DELETE from pengeluaran where kode='" + kode + "'");
                if (rowsDeleted > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan atau sudah dihapus");
                }
                statement.close();
            } catch (Exception t) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + t.getMessage());
            }
            datatable();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
         int selectedRow = tabel.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit terlebih dahulu.");
        return;
    }

    String tanggal = "";
    if (txttanggal.getDate() != null) {
        SimpleDateFormat sd = new SimpleDateFormat("yyyy-MM-dd");
        tanggal = sd.format(txttanggal.getDate());
    }

    try {
        String sql = "UPDATE pengeluaran SET jumlah=?, tanggal=?, keterangan=? WHERE kode=?";
        PreparedStatement preparedStatement = Conek.GetConnection().prepareStatement(sql);

        preparedStatement.setString(1, txtjumlah.getText());
        preparedStatement.setString(2, tanggal);
        preparedStatement.setString(3, txtketerangan.getText());
        preparedStatement.setString(4, txtkode.getText());

        int affectedRows = preparedStatement.executeUpdate();

        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan kode yang diubah ada dalam database.");
        }

        preparedStatement.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saat mengubah data: " + e.getMessage());
    }

    datatable();
    }//GEN-LAST:event_btn_editActionPerformed

